namespace Admin.NET.Application;

/// <summary>
/// 任务表输出参数
/// </summary>
public class AssignmentOutput
{
    /// <summary>
    /// 主键Id
    /// </summary>
    public long Id { get; set; }
    
    /// <summary>
    /// 备注
    /// </summary>
    public string MeMo { get; set; }
    
    /// <summary>
    /// 前置任务
    /// </summary>
    public long PredecessorTaskId { get; set; }
    
    /// <summary>
    /// 前置任务名称
    /// </summary>
    public string PredecessorTaskName { get; set; }
    
    /// <summary>
    /// 任务名称
    /// </summary>
    public string AssignmentName { get; set; }
    
    /// <summary>
    /// 所属项目id
    /// </summary>
    public long ProjectId { get; set; }
    
    /// <summary>
    /// 编码
    /// </summary>
    public string Sno { get; set; }
    
    /// <summary>
    /// 任务所属人
    /// </summary>
    public long BelongtoId { get; set; }
    /// <summary>
    /// 任务所属人名称
    /// </summary>
    public string RoleName { get; set; }

    /// <summary>
    /// 紧急程度
    /// </summary>
    public int Prioriry { get; set; }
    
    /// <summary>
    /// 重要程度
    /// </summary>
    public int Instancy { get; set; }
    
    /// <summary>
    /// 任务内容
    /// </summary>
    public string ConTent { get; set; }
    
    /// <summary>
    /// 状态
    /// </summary>
    public int Status { get; set; }
    
    /// <summary>
    /// 开始时间
    /// </summary>
    public DateTime StartTime { get; set; }
    
    /// <summary>
    /// 预计完成天数
    /// </summary>
    public int ProjectedCompletionTime { get; set; }
    
    /// <summary>
    /// 已用天数
    /// </summary>
    public int DaysUsed { get; set; }
    
    /// <summary>
    /// 实际完成时间
    /// </summary>
    public DateTime ActyalTime { get; set; }
    
    /// <summary>
    /// 任务完成评价
    /// </summary>
    public string Appraise { get; set; }
    
    /// <summary>
    /// 效率评价等级
    /// </summary>
    public int EfficiencyLevel { get; set; }
    
    /// <summary>
    /// 质量评价等级
    /// </summary>
    public int QualityLevel { get; set; }
    
    /// <summary>
    /// 创建时间
    /// </summary>
    public DateTime? CreateTime { get; set; }
    
    /// <summary>
    /// 更新时间
    /// </summary>
    public DateTime? UpdateTime { get; set; }
    
    /// <summary>
    /// 创建者Id
    /// </summary>
    public long? CreateUserId { get; set; }
    
    /// <summary>
    /// 创建者姓名
    /// </summary>
    public string? CreateUserName { get; set; }
    
    /// <summary>
    /// 修改者Id
    /// </summary>
    public long? UpdateUserId { get; set; }
    
    /// <summary>
    /// 修改者姓名
    /// </summary>
    public string? UpdateUserName { get; set; }
    
    /// <summary>
    /// 软删除
    /// </summary>
    public bool IsDelete { get; set; }
    
    /// <summary>
    /// 乐观锁
    /// </summary>
    public int ReVision { get; set; }
    
    }
