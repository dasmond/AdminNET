<script lang="ts" setup>
	import { ref,onMounted } from "vue";
	import { getDictDataItem as di, getDictDataList as dl } from '/@/utils/dict-utils';
	import { ElMessage } from "element-plus";
	import type { FormRules } from "element-plus";
	import { addProcessInstanceRecord, updateProcessInstanceRecord, detailProcessInstanceRecord } from "/@/api/main/processInstanceRecord";

	//父级传递来的参数
	var props = defineProps({
		title: {
		type: String,
		default: "",
	},
	});
	//父级传递来的函数，用于回调
	const emit = defineEmits(["reloadTable"]);
	const ruleFormRef = ref();
	const isShowDialog = ref(false);
	const ruleForm = ref<any>({});
	//自行添加其他规则
	const rules = ref<FormRules>({
		businessKey: [{required: true, message: '请输入业务ID！', trigger: 'blur',},],
		processInstanceId: [{required: true, message: '请输入流程实例id！', trigger: 'blur',},],
		flowId: [{required: true, message: '请输入流程模板ID！', trigger: 'blur',},],
		flowTaskId: [{required: true, message: '请输入当前流程任务id！', trigger: 'blur',},],
		flowTaskName: [{required: true, message: '请输入当前流程任务名称！', trigger: 'blur',},],
		startUserName: [{required: true, message: '请输入操作人！', trigger: 'blur',},],
		startUserId: [{required: true, message: '请输入操作人id！', trigger: 'blur',},],
		approvalResults: [{required: true, message: '请输入审批结果！', trigger: 'blur',},],
		opinionOfApprover: [{required: true, message: '请输入审批人意见！', trigger: 'blur',},],
		roleGroup: [{required: true, message: '请输入角色组！', trigger: 'blur',},],
		processBranchId: [{required: true, message: '请输入流程分支业务id！', trigger: 'blur',},],
		reVision: [{required: true, message: '请输入乐观锁！', trigger: 'blur',},],
	});

	// 打开弹窗
	const openDialog = async (row: any) => {
		// ruleForm.value = JSON.parse(JSON.stringify(row));
		// 改用detail获取最新数据来编辑
		let rowData = JSON.parse(JSON.stringify(row));
		if (rowData.id)
			ruleForm.value = (await detailProcessInstanceRecord(rowData.id)).data.result;
		else
			ruleForm.value = rowData;
		isShowDialog.value = true;
	};

	// 关闭弹窗
	const closeDialog = () => {
		emit("reloadTable");
		isShowDialog.value = false;
	};

	// 取消
	const cancel = () => {
		isShowDialog.value = false;
	};

	// 提交
	const submit = async () => {
		ruleFormRef.value.validate(async (isValid: boolean, fields?: any) => {
			if (isValid) {
				let values = ruleForm.value;
				if (ruleForm.value.id == undefined || ruleForm.value.id == null || ruleForm.value.id == "" || ruleForm.value.id == 0) {
					await addProcessInstanceRecord(values);
				} else {
					await updateProcessInstanceRecord(values);
				}
				closeDialog();
			} else {
				ElMessage({
					message: `表单有${Object.keys(fields).length}处验证失败，请修改后再提交`,
					type: "error",
				});
			}
		});
	};







	// 页面加载时
	onMounted(async () => {
	});

	//将属性或者函数暴露给父组件
	defineExpose({ openDialog });
